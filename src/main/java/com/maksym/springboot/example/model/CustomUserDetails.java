package com.maksym.springboot.example.model;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.ArrayList;
import java.util.Collection;
import java.util.Collections;
import java.util.List;
import java.util.Locale;

import static org.springframework.security.core.authority.AuthorityUtils.createAuthorityList;

/**
 * Created by Maksym_Hridin on 2/28/2017.
 */
public class CustomUserDetails implements UserDetails {

    private static final long serialVersionUID = 987534894L;

    private static final String ROLE_PREFIX = "ROLE_";

    private String id;

    private String username;

    private String password;

    private String fullName;

    private String email;

    private List<Role> roles;

    public CustomUserDetails() {
        this.roles = Collections.singletonList(Role.ADMIN);
    }

    /**
     * Gets id.
     *
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * Sets id.
     *
     * @param id the id
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * Sets password.
     *
     * @param password the password
     */
    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public String getPassword() {
        return password;
    }

    /**
     * Sets username.
     *
     * @param username the username
     */
    public void setUsername(String username) {
        this.username = username;
    }

    @Override
    public String getUsername() {
        return username;
    }

    /**
     * Gets full name.
     *
     * @return the full name
     */
    public String getFullName() {
        return fullName;
    }

    /**
     * Sets full name.
     *
     * @param fullName the full name
     */
    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    /**
     * Gets email.
     *
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * Sets email.
     *
     * @param email the email
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * Gets roles.
     *
     * @return the roles
     */
    public List<Role> getRoles() {
        return roles;
    }

    /**
     * Sets roles.
     *
     * @param roles the roles
     */
    public void setRoles(List<Role> roles) {
        this.roles = roles;
    }

     @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        ArrayList<String> rolesWithPrefixes = new ArrayList<>();
        if (roles != null) {
            roles.forEach(role -> rolesWithPrefixes.add(addPrefix(role)));
        }
        return createAuthorityList(rolesWithPrefixes.toArray(new String[rolesWithPrefixes.size()]));
    }

    @Override
    public boolean isAccountNonExpired() {
        return false;
    }

    @Override
    public boolean isAccountNonLocked() {
        return false;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return false;
    }

    @Override
    public boolean isEnabled() {
        return false;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }

        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        CustomUserDetails that = (CustomUserDetails) o;

        return new EqualsBuilder()
                .append(id, that.id)
                .isEquals();
    }

    @Override
    public int hashCode() {
        return new HashCodeBuilder(17, 37)
                .append(id)
                .toHashCode();
    }

    private String addPrefix(Role role) {
        return ROLE_PREFIX + role.toString().toUpperCase(Locale.ENGLISH);
    }
}
